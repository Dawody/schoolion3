    private void RefreshTextFields(ResultSet rslt)
    {
        ArrayList<String> row = new ArrayList<String>(9);
        try {
            while(rslt.next())
            {
                for (int i = 1; i <= 9; i++) {
                    row.add(rslt.getString(i));
                }
            }
            TeacherName_txt.setText(row.get(0)+" "+row.get(1)+" "+row.get(2));
            TeacherID_txt.setText(row.get(3));
            TeacherAddress_txt.setText(row.get(4));
            TeacherPhone_txt.setText(row.get(5));
            TeacherEmail_txt.setText(row.get(6));
            TeacherPosition_txt.setText(row.get(7));
            TeacherBus_Number_txt.setText(row.get(8));
        } catch (SQLException ex) {
            Logger.getLogger(TeacherFram.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void ViewTeacherSchedule()
    {
            try {
            DefaultTableModel mytable = (DefaultTableModel)TeacherScheduleTable.getModel();
            mytable.setValueAt("  Saturday", 0,0);
            mytable.setValueAt("   Sunday", 1,0 );
            mytable.setValueAt("   Monday", 2,0 );
            mytable.setValueAt("  Tuesday", 3,0 );
            mytable.setValueAt(" Wednesday", 4,0 );
            mytable.setValueAt("   Thursday",5,0 );
            mytable.setValueAt("    Friday", 6,0 );
            String classid,subcode,teachid,name = "";
            int day = 0,period = 0;
            res = q.getClassCodeTeachid(Tid);
            ResultSet res1,res2;
            while(res.next()) {
            classid = res.getString(1);
            subcode = res.getString(2);
            teachid = res.getString(3);
            res1 = q.getSubjectName(subcode);
            while(res1.next()){
            name = res1.getString(1);
            res2 = q.getDayPeriod(teachid);
            while(res2.next()) {
            day = Integer.parseInt(res2.getString(1));
            period = Integer.parseInt(res2.getString(2));
            }
            if(!name.isEmpty() && day != 0 && period != 0) {
            mytable.setValueAt("  " + classid + "   " + name, day-1, period);
            name = "";
            day = 0;
            period = 0;
            }
            }
            }
            } catch (SQLException ex) {
            Logger.getLogger(TeacherFram.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    
    private void fillTeacherClassesComboBox()
    {
        try {
            res = q.getTeacherClasses(Tid);
            while(res.next())
            {
                TeacherClassesComboBox.addItem(res.getString("cid"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(TeacherFram.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void fillTeacherSubjectsComboBox()
    {
        try {
            res = q.getSubjectOfTeacherClass(Tid, (String) TeacherClassesComboBox.getSelectedItem());
            while(res.next())
            {
                SubjectCompobox.addItem(res.getString("code"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(TeacherFram.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
