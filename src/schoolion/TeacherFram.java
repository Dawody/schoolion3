    private void EditGradesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditGradesButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel mytable = (DefaultTableModel)ClassTable.getModel();
        if(mytable.getColumnCount()== 0 || mytable.getColumnCount() == 6 ) {
            return;
        }
        mytable.setColumnCount(7);
        JTableHeader th = ClassTable.getTableHeader();
        TableColumnModel tcm = th.getColumnModel();
        tcm.getColumn(0).setHeaderValue("SID");
        tcm.getColumn(1).setHeaderValue("YWork(1)");
        tcm.getColumn(2).setHeaderValue("Mid (1)");
        tcm.getColumn(3).setHeaderValue("Final (1)");
        tcm.getColumn(4).setHeaderValue("YWork(2)");
        tcm.getColumn(5).setHeaderValue("Mid (2)");
        tcm.getColumn(6).setHeaderValue("Final (2)");
        th.repaint();
        ArrayList<String> arr = new ArrayList<>(6);
        arr.add(0,"yw1");
        arr.add(1,"mt1");
        arr.add(2,"f1");
        arr.add(3,"yw2");
        arr.add(4,"mt2");
        arr.add(5,"f2");
        if(mytable.getColumnCount()== 7 ) {
          for (int i = 0; i < mytable.getRowCount(); i++) {
                 for (int j = 1; j <= 6; j++) {
                        q.UpdateGrades((String) mytable.getValueAt(i, 0)
                                , arr.get(j-1), (String) mytable.getValueAt(i, j),
                                (String) SubjectCompobox.getSelectedItem());
                    }
              }
        }
    }//GEN-LAST:event_EditGradesButtonActionPerformed

