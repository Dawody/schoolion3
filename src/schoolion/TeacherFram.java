    private void RefreshClassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RefreshClassButtonActionPerformed
        try {
            // TODO add your handling code here:
            String cid = (String) TeacherClassesComboBox.getSelectedItem();
            res = q.getClassInfo(cid);
            while(res.next())   {
                ClassID_txt.setText(res.getString("cid"));
                NumStudents_txt.setText(res.getString("csize"));
                ScienceField_txt.setText(res.getString("sciencefield"));
                Capacity_txt.setText(res.getString("capacity"));
                LevelClass_txt.setText(res.getString("level"));
                if("0".equals(LevelClass_txt.getText()))
                    LevelClass_txt.setText("Normal");
                else
                    LevelClass_txt.setText("High");
            }
        } catch (SQLException ex) {
            Logger.getLogger(TeacherFram.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_RefreshClassButtonActionPerformed

    private void StudentsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StudentsButtonActionPerformed
        try {
            // TODO add your handling code here:
            String cid = (String) TeacherClassesComboBox.getSelectedItem();
            DefaultTableModel mytable = (DefaultTableModel)ClassTable.getModel();
            res = q.getAllStudentInClass(cid);
            mytable.setColumnCount(0);
            mytable.setRowCount(0);
            mytable.setColumnCount(6);
            JTableHeader th = ClassTable.getTableHeader();
            TableColumnModel tcm = th.getColumnModel();
            tcm.getColumn(0).setHeaderValue("Student ID");
            tcm.getColumn(1).setHeaderValue("Fname");
            tcm.getColumn(2).setHeaderValue("Sname");
            tcm.getColumn(3).setHeaderValue("Lname");
            tcm.getColumn(4).setHeaderValue("G-Phone");
            tcm.getColumn(5).setHeaderValue("G-Email");
            th.repaint();
            while(res.next()){
               mytable.insertRow(mytable.getRowCount(), new Object[]{
               res.getString(1),
                res.getString(2),
                res.getString(3),
                res.getString(4),
                res.getString(5),
                res.getString(6)
            });          
            }
        } catch (SQLException ex) {
            Logger.getLogger(TeacherFram.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_StudentsButtonActionPerformed

